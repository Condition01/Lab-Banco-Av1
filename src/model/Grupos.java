package model;

public class Grupos {
	
	private String grupo;
	private int codTime;
	
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public int getCodTime() {
		return codTime;
	}
	public void setCodTime(int codTime) {
		this.codTime = codTime;
	}
	
	
}
